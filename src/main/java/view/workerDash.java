    private void DispatchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DispatchButtonActionPerformed
        // TODO add your handling code here:
          String orderId = EnterID.getText().trim();
       if (orderId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please , enter the order ID", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }
        // Confirmar antes de eliminar
        int confirmation = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to ship this order?",
            "Confirm order output ",
            JOptionPane.YES_NO_OPTION);
            
        if (confirmation == JOptionPane.YES_OPTION) {
            dispatchOrder dispOrder = new dispatchOrder();
            boolean success = dispOrder.dispatchOrderById(orderId);
            
            if (success) {
                JOptionPane.showMessageDialog(null, 
                    "Order shipped successfully", 
                    "Exit", 
                    JOptionPane.INFORMATION_MESSAGE);
                    
                // Limpiar los campos
                EnterID.setText("");
                txtnamepro.setText("");
                txtamount.setText("");
                txtpaytotal.setText("");
                
                
                
             } else {
                JOptionPane.showMessageDialog(null, 
                    "Could not delete order", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
            
           
        }
    }//GEN-LAST:event_DispatchButtonActionPerformed

    private void EnterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterButtonActionPerformed
        // TODO add your handling code here:
          String id_order = EnterID.getText().trim();
        
        //validaciones
        if (id_order.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please, complete the field.", "Empty Fields" , JOptionPane.WARNING_MESSAGE);
        return;
        }
        if (id_order.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(null, "The ID cannot have letters, only numbers" ,"Length error" , JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        
        // Instancia de la clase que maneja el acceso a orders
         orderSearch us = new orderSearch();
         boolean idExitoso = us.accesoOrder(id_order);
          if (idExitoso) {
         mostrarOrder access = new mostrarOrder();
         access.obtenerinfoOrder(id_order, txtnamepro, txtamount, txtpaytotal);
        } else {
        JOptionPane.showMessageDialog(null, "Order ID not found or invalid.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EnterButtonActionPerformed

